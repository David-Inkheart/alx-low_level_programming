#include <stdio.h>
/**
 * main - Entry point
 *
 * Description: 'a program that prints all single digit numbers of base 10'
 * It should start from 0
 * The printout is followed by a newline
 * Return: Always 0 (Success)
 */
int main(void)
{
	int i = 0;

	while (i <= 9)
{
	printf("%d", i);
	i++;
}
{
	printf("\n");
}
	return (0);
}
